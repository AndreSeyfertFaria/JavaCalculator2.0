    private void GetActivePageIndex(Integer pageIndex) {
        String selected_text = jComboBox1.getItemAt(jComboBox1.getSelectedIndex());
        
        if (selected_text == "Basic Mode"){
            pageIndex = 0;
        }               
        else if (selected_text == "Advanced Mode"){
            pageIndex = 1;  
        }
        else if (selected_text == "Financial Mode"){
            pageIndex = 2;
        }
         else if (selected_text == "Programming Mode"){
            pageIndex = 3;
        }
        ActivePageIndex = pageIndex;
    }

